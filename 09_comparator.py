"""
Los comparadores se utilizan mucho para poder diferenciar dos variables normalmente numéricas o strings. Por ejemplo ver si un número es mayor que otro. 
"""

# > Operador mayor que
print(7>3)

# < Operador menor que
print(5<6)

# >= Operador mayor igual
print(2>=1)

# <= Operador menor igual
print(1<=2)

# == Operador de igualdad
print(6==6)

# != Operador de diferente 
print(6!=10)

print("Apple" == 'Apple')

age = 18
print(age>=18) 